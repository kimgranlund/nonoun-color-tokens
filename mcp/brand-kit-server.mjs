#!/usr/bin/env node
// brand-kit-server.mjs — a zero-dependency MCP (Model Context Protocol) server that serves a NONOUN
// brand kit (the generated color tokens) to AI agents: Claude Code / Claude Design, Cursor, VS Code,
// ChatGPT — anything that speaks MCP. Pure Node, no `npm install` — just `node brand-kit-server.mjs`.
//
// It reads a sibling `brand-kit.json` (the resolved tokens, produced by "Color Tokens by NONOUN") and
// exposes the palettes, the perceptual ramps, and the resolved 37-role semantic layer (light + dark) as
// MCP resources + tools + a usage prompt, so an agent builds with the brand's EXACT tokens instead of
// guessing a colour.
//
// Transport: JSON-RPC 2.0 over stdio, newline-delimited. (All logging goes to STDERR — writing anything
// non-protocol to stdout corrupts the stream.)

import { readFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const HERE = dirname(fileURLToPath(import.meta.url));
const PROTOCOL_VERSION = "2025-06-18";
const SERVER = { name: "nonoun-brand-kit", version: "0.1.0" };

// ── load the brand kit ────────────────────────────────────────────────────────────────────────
// path: argv[2] → $BRAND_KIT → ./brand-kit.json (next to this server). Fail loud on stderr, never stdout.
const KIT_PATH = process.argv[2] || process.env.BRAND_KIT || resolve(HERE, "brand-kit.json");
let kit;
try {
  kit = JSON.parse(readFileSync(KIT_PATH, "utf8"));
} catch (e) {
  process.stderr.write(`[brand-kit] could not read ${KIT_PATH}: ${e.message}\n`);
  process.exit(1);
}
const palettes = Array.isArray(kit.palettes) ? kit.palettes : [];
const roles = kit.roles || {};
const slugOf = (s) => String(s || "").toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/(^-|-$)/g, "");
const findPalette = (name) => {
  const q = slugOf(name);
  return palettes.find((p) => slugOf(p.name) === q || slugOf(p.slug) === q) || null;
};

// ── domain helpers (the actual brand-kit logic the tools wrap) ───────────────────────────────────
const hexToRgb = (h) => { const m = String(h).replace("#", ""); return [0, 2, 4].map((i) => parseInt(m.slice(i, i + 2) || "0", 16)); };
function nearestToken(hex) {
  const [r, g, b] = hexToRgb(hex);
  let best = null;
  for (const p of palettes) for (const s of (p.ramp || [])) {
    const [r2, g2, b2] = hexToRgb(s.hex);
    const d = (r - r2) ** 2 + (g - g2) ** 2 + (b - b2) ** 2;
    if (!best || d < best.d) best = { d, palette: p.name, stop: s.stop, hex: s.hex };
  }
  return best && { palette: best.palette, stop: best.stop, hex: best.hex, distance: Math.round(Math.sqrt(best.d)) };
}
const semanticFor = (scheme) => {
  const out = {};
  for (const [slug, rs] of Object.entries(roles)) for (const [key, v] of Object.entries(rs)) out[`${slug}/${key}`] = v[scheme] ?? v.light;
  return out;
};
const usageGuide = () =>
  `# ${kit.name || "Brand Kit"} — usage\n\n` +
  `Generated by ${kit.generator || "Color Tokens by NONOUN"}. Apply these tokens; never invent raw hex.\n\n` +
  `- **Surfaces / backgrounds** → the \`*/surface*\` and \`*/background\` roles (resolve per light/dark).\n` +
  `- **Accents** → a palette's prime role (e.g. \`primary/primary\`) and its \`*Dim/Bright/Low/High\` variants.\n` +
  `- **Text on accents** → the \`*/on*\` roles (already contrast-aware).\n` +
  `- **Borders/dividers** → \`*/outline*\`; **overlays** → \`*/scrim*\`.\n` +
  `- Pass a \`scheme\` of \`light\` or \`dark\`; resolve_token gives the exact hex.\n` +
  `- Match an existing colour with \`nearest_token\` before introducing a new one.\n`;

// ── MCP surface: tools, resources, prompts ───────────────────────────────────────────────────────
const TOOLS = [
  { name: "list_palettes", description: "List the brand's palettes with their identity colour.",
    inputSchema: { type: "object", properties: {} },
    run: () => palettes.map((p) => ({ name: p.name, key: p.key, stops: (p.ramp || []).length })) },
  { name: "get_ramp", description: "The full tonal ramp (stops → hex) for one palette.",
    inputSchema: { type: "object", properties: { palette: { type: "string" } }, required: ["palette"] },
    run: (a) => { const p = findPalette(a.palette); return p ? { name: p.name, ramp: p.ramp } : { error: `no palette "${a.palette}"` }; } },
  { name: "resolve_token", description: "Resolve a semantic role to its hex in a scheme. role = \"palette/roleKey\" (or palette + role).",
    inputSchema: { type: "object", properties: { palette: { type: "string" }, role: { type: "string" }, scheme: { type: "string", enum: ["light", "dark"] } } },
    run: (a) => {
      const scheme = a.scheme === "dark" ? "dark" : "light";
      let slug = a.palette, key = a.role;
      if (!a.palette && a.role && a.role.includes("/")) { const [pp, ...rest] = a.role.split("/"); slug = pp; key = rest.join("/"); }
      const r = (roles[slugOf(slug)] || {})[key];
      return r ? { palette: slug, role: key, scheme, hex: r[scheme] ?? r.light } : { error: `no role "${slug}/${key}"` };
    } },
  { name: "get_semantic", description: "All resolved semantic roles for a scheme, as { \"palette/role\": hex }.",
    inputSchema: { type: "object", properties: { scheme: { type: "string", enum: ["light", "dark"] } } },
    run: (a) => semanticFor(a.scheme === "dark" ? "dark" : "light") },
  { name: "nearest_token", description: "The brand token closest to a given hex (so the agent reuses the system instead of inventing a colour).",
    inputSchema: { type: "object", properties: { hex: { type: "string" } }, required: ["hex"] },
    run: (a) => nearestToken(a.hex) || { error: "no palettes" } },
];

const RESOURCES = [
  { uri: "brand://kit", name: "Brand kit (full)", mimeType: "application/json", read: () => JSON.stringify(kit, null, 2) },
  { uri: "brand://palettes", name: "Palettes + ramps", mimeType: "application/json", read: () => JSON.stringify(palettes, null, 2) },
  { uri: "brand://semantic/light", name: "Semantic roles (light)", mimeType: "application/json", read: () => JSON.stringify(semanticFor("light"), null, 2) },
  { uri: "brand://semantic/dark", name: "Semantic roles (dark)", mimeType: "application/json", read: () => JSON.stringify(semanticFor("dark"), null, 2) },
  { uri: "brand://guide", name: "Usage guide", mimeType: "text/markdown", read: usageGuide },
];

const PROMPTS = [
  { name: "apply_brand", description: "Guidelines for applying this brand kit's tokens.",
    get: () => ({ description: "Apply the brand kit", messages: [{ role: "user", content: { type: "text", text: usageGuide() } }] }) },
];

// ── JSON-RPC plumbing ────────────────────────────────────────────────────────────────────────────
const send = (msg) => process.stdout.write(JSON.stringify(msg) + "\n");
const reply = (id, result) => send({ jsonrpc: "2.0", id, result });
const fail = (id, code, message) => send({ jsonrpc: "2.0", id, error: { code, message } });
const textResult = (obj) => ({ content: [{ type: "text", text: typeof obj === "string" ? obj : JSON.stringify(obj, null, 2) }] });

function handle(msg) {
  const { id, method, params } = msg;
  const isRequest = id !== undefined && id !== null;
  switch (method) {
    case "initialize":
      return reply(id, { protocolVersion: PROTOCOL_VERSION, capabilities: { tools: {}, resources: {}, prompts: {} }, serverInfo: SERVER, instructions: `Brand kit "${kit.name || ""}" from ${kit.generator || "NONOUN"}. Use resolve_token / get_ramp / nearest_token; read brand://guide first.` });
    case "notifications/initialized": return; // notification, no reply
    case "ping": return isRequest && reply(id, {});
    case "tools/list": return reply(id, { tools: TOOLS.map(({ name, description, inputSchema }) => ({ name, description, inputSchema })) });
    case "tools/call": {
      const t = TOOLS.find((x) => x.name === (params && params.name));
      if (!t) return fail(id, -32602, `unknown tool: ${params && params.name}`);
      try { return reply(id, textResult(t.run((params && params.arguments) || {}))); }
      catch (e) { return reply(id, { ...textResult(`error: ${e.message}`), isError: true }); }
    }
    case "resources/list": return reply(id, { resources: RESOURCES.map(({ uri, name, mimeType }) => ({ uri, name, mimeType })) });
    case "resources/read": {
      const r = RESOURCES.find((x) => x.uri === (params && params.uri));
      if (!r) return fail(id, -32602, `unknown resource: ${params && params.uri}`);
      return reply(id, { contents: [{ uri: r.uri, mimeType: r.mimeType, text: r.read() }] });
    }
    case "prompts/list": return reply(id, { prompts: PROMPTS.map(({ name, description }) => ({ name, description })) });
    case "prompts/get": {
      const p = PROMPTS.find((x) => x.name === (params && params.name));
      if (!p) return fail(id, -32602, `unknown prompt: ${params && params.name}`);
      return reply(id, p.get((params && params.arguments) || {}));
    }
    default:
      if (isRequest) return fail(id, -32601, `method not found: ${method}`);
  }
}

// newline-delimited JSON-RPC over stdin
let buf = "";
process.stdin.setEncoding("utf8");
process.stdin.on("data", (chunk) => {
  buf += chunk;
  let nl;
  while ((nl = buf.indexOf("\n")) >= 0) {
    const line = buf.slice(0, nl).trim();
    buf = buf.slice(nl + 1);
    if (!line) continue;
    try { handle(JSON.parse(line)); }
    catch (e) { process.stderr.write(`[brand-kit] bad message: ${e.message}\n`); }
  }
});
process.stdin.on("end", () => process.exit(0));
process.stderr.write(`[brand-kit] serving "${kit.name || "Brand Kit"}" (${palettes.length} palettes) over MCP stdio\n`);
