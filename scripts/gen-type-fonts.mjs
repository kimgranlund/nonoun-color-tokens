// gen-type-fonts.mjs — fetch the LATIN subset of the typography fonts and inline them as base64 @font-face
// rules, so the Typography specimen renders in the real faces EVERYWHERE — including offline and inside
// the Figma plugin (manifest networkAccess:"none", where the Google Fonts CDN is hard-blocked). The
// emitted src/ui/type-fonts.js is a COMMITTED generated asset (like figma-plugin-assets.js / mcp-assets.js);
// run this manually only when the font set changes:  node scripts/gen-type-fonts.mjs
//
// Each family's `latin` block from Google's css2 is already a small variable-font subset (~30–50KB woff2),
// so the whole set is ~170KB woff2 → ~230KB base64 — a reasonable, offline-proof embed.
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname } from "node:path";

const ROOT = dirname(dirname(fileURLToPath(import.meta.url)));
const UA = "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120 Safari/537.36";

// the families the treatments use (engine/type.mjs), each with its variable weight range.
const FAMILIES = [
  { name: "Inter", axis: "wght@400..900", weight: "400 900" },
  { name: "Inter Tight", axis: "wght@400..900", weight: "400 900" },
  { name: "Source Serif 4", axis: "wght@400..900", weight: "400 900" },
  { name: "JetBrains Mono", axis: "wght@400..800", weight: "400 800" },
];

// pull the LATIN @font-face block's woff2 url from a css2 response (the block whose unicode-range starts at
// U+0000 — basic Latin; that's all the fixed specimen samples need).
function latinWoff2Url(css) {
  const blocks = css.split("@font-face").slice(1);
  for (const b of blocks) {
    const ur = (b.match(/unicode-range:\s*([^;]+);/) || [])[1] || "";
    if (/U\+0000/.test(ur)) {
      const url = (b.match(/url\((https:\/\/[^)]+\.woff2)\)/) || [])[1];
      if (url) return url;
    }
  }
  // fallback: the last woff2 in the file (Google emits latin last)
  const all = [...css.matchAll(/url\((https:\/\/[^)]+\.woff2)\)/g)].map((m) => m[1]);
  return all[all.length - 1] || null;
}

const faces = [];
let totalKb = 0;
for (const f of FAMILIES) {
  const cssUrl = `https://fonts.googleapis.com/css2?family=${encodeURIComponent(f.name).replace(/%20/g, "+")}:${f.axis}&display=swap`;
  const css = await fetch(cssUrl, { headers: { "User-Agent": UA } }).then((r) => r.text());
  const woff2 = latinWoff2Url(css);
  if (!woff2) throw new Error("no latin woff2 for " + f.name);
  const buf = Buffer.from(await fetch(woff2, { headers: { "User-Agent": UA } }).then((r) => r.arrayBuffer()));
  totalKb += buf.length / 1024;
  const b64 = buf.toString("base64");
  faces.push(
    `@font-face{font-family:'${f.name}';font-style:normal;font-weight:${f.weight};font-display:swap;` +
    `src:url(data:font/woff2;base64,${b64}) format('woff2');}`,
  );
  console.log(`  ${f.name.padEnd(16)} ${(buf.length / 1024).toFixed(0)} KB`);
}

const out = `// GENERATED by scripts/gen-type-fonts.mjs — DO NOT EDIT.
// The Latin subset of the typography fonts, inlined as base64 @font-face so the specimen renders in the
// real faces offline + in the Figma plugin (networkAccess:"none"). Regenerate: node scripts/gen-type-fonts.mjs
export const TYPE_FONTS_CSS = ${JSON.stringify(faces.join("\n"))};
`;
writeFileSync(`${ROOT}/src/ui/type-fonts.js`, out);
console.log(`wrote src/ui/type-fonts.js  (${(out.length / 1024).toFixed(0)} KB · fonts ${totalKb.toFixed(0)} KB woff2)`);
