// gen-mcp-assets.mjs — inline the Brand-Kit MCP into the app.
//
// The mcp/ folder (brand-kit-server.mjs + README.md) is the single source of truth; this generates
// src/ui/mcp-assets.js so the tool can HAND the user a ready-to-run MCP package as a download (the
// zero-dep server + their resolved brand-kit.json). Regenerate after editing:  node gen-mcp-assets.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname } from "node:path";

const HERE = dirname(fileURLToPath(import.meta.url));
const MDIR = `${HERE}/../mcp`;
const server = readFileSync(`${MDIR}/brand-kit-server.mjs`, "utf8");
const core = readFileSync(`${MDIR}/brand-kit-core.mjs`, "utf8"); // the server imports this sibling — ship both
const readme = readFileSync(`${MDIR}/README.md`, "utf8");

const out = `// GENERATED by gen-mcp-assets.mjs from mcp/ — DO NOT EDIT.
// The zero-dependency Brand-Kit MCP server + README, inlined so the tool can hand the user a
// ready-to-run MCP package (server + their brand-kit.json). Single source of truth is mcp/; regenerate
// with  node gen-mcp-assets.mjs
export const MCP_BRAND_KIT = {
  server: ${JSON.stringify(server)},
  core: ${JSON.stringify(core)},
  readme: ${JSON.stringify(readme)},
};
`;
writeFileSync(`${HERE}/../src/ui/mcp-assets.js`, out);
console.log("wrote src/ui/mcp-assets.js", (out.length / 1024).toFixed(1) + " KB");
