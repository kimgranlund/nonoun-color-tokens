// gen-figma-assets.mjs — inline the HCT Semantic Binder plugin into the app.
//
// The figma-plugin cell (figma/binder/figma-semantic-binder/) is the single source
// of truth; this generates ui-app/figma-plugin-assets.js so the tool can HAND the plugin
// to the user as a download (the live raw->semantic alias cascade native JSON import
// can't do). Regenerate after editing the plugin:  node gen-figma-assets.mjs
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname } from "node:path";

const HERE = dirname(fileURLToPath(import.meta.url));
const PDIR = `${HERE}/../figma/binder/figma-semantic-binder`;
const manifest = readFileSync(`${PDIR}/manifest.json`, "utf8");
const code = readFileSync(`${PDIR}/code.js`, "utf8");

const out = `// GENERATED by gen-figma-assets.mjs from figma/binder/figma-semantic-binder/ — DO NOT EDIT.
// The HCT Semantic Binder plugin (manifest + runtime), inlined so the tool can hand it to
// the user as a download. Single source of truth is the figma-plugin cell; regenerate with
//   node gen-figma-assets.mjs
export const FIGMA_PLUGIN = {
  name: ${JSON.stringify(JSON.parse(manifest).name)},
  manifest: ${JSON.stringify(manifest)},
  code: ${JSON.stringify(code)},
};
`;
writeFileSync(`${HERE}/../src/ui/figma-plugin-assets.js`, out);
console.log("wrote src/ui/figma-plugin-assets.js", (out.length / 1024).toFixed(1) + " KB");
